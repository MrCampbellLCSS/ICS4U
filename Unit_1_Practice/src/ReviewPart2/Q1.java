package ReviewPart2;

public class Q1 {

	public static void main(String[] args) {
		int x = 0;
		while (x <= 5) {
			System.out.println(x);
			x++;
		}

		x = 0;
		do {
			System.out.println(x);
			x++;
		} while (x < 5);
		
		for (int i = 0; i < 6; i++) {
			System.out.println(i);
		}
	}

}
